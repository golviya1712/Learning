package com.jd.interview.hashcodeequal;

public class Human {

	int adharNumber;
	String name;

	public Human(int adharNumber, String name) {
		this.adharNumber = adharNumber;
		this.name = name;
	}

	public int getAdharNumber() {
		return adharNumber;
	}

	public void setAdharNumber(int adharNumber) {
		this.adharNumber = adharNumber;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
